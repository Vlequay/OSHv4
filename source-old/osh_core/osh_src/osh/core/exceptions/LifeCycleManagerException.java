package osh.core.exceptions;

public class LifeCycleManagerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4798222209596818261L;

	public LifeCycleManagerException() {
		// NOTHING
	}

	public LifeCycleManagerException(String arg0) {
		super(arg0);
		// NOTHING
	}

	public LifeCycleManagerException(Throwable arg0) {
		super(arg0);
		// NOTHING
	}

	public LifeCycleManagerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		// NOTHING
	}
}
